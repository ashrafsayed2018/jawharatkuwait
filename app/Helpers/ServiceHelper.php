    /**
     * Get service by slug from fallback data
     * 
     * @param string $slug
     * @return array|null
     */
    public static function getFallbackServiceBySlug(string $slug): ?array
    {
        $services = self::getFallbackServices();
        
        foreach ($services as $service) {
            if ($service['slug'] === $slug) {
                return $service;
            }
        }
        
        return null;
    }
